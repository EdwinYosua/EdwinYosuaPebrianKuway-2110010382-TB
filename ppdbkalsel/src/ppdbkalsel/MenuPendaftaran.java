    //Untuk eksekusi sql command pencari data
    private ResultSet cariData(String sql_comm) throws SQLException {
        Connection conn = MysqlKonek.koneksiDb();
        Statement st = (Statement) conn.createStatement();
        ResultSet rs = st.executeQuery(sql_comm);
        return rs;
    }
    
    //tampilkan hasil pencarian di table Siswa
    private void hasilCariTableSiswaShow(ResultSet rs) throws SQLException {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NISN SISWA");
        model.addColumn("NAMA SISWA");
        model.addColumn("JENIS KELAMIN");
        model.addColumn("TANGGAL LAHIR");
        model.addColumn("TEMPAT LAHIR");
        model.addColumn("ALAMAT SISWA");
        model.addColumn("NIK ORTU");
        model.addColumn("NAMA ORTU");              
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model.setRowCount(0);

        while(rs.next()) {
            Object [] data = {
                rs.getString("nisn_siswa"),
                rs.getString("nama_siswa"),
                rs.getString("jk"),
                rs.getString("tanggal_lahir"),
                rs.getString("tempat_lahir"),
                rs.getString("alamat_siswa"),
                rs.getString("nik_ortu"),
                rs.getString("nama_ortu")
            }; 
            model.addRow(data);
            tableSiswa.setModel(model);
        }
    }
    
    //tampilkan hasil pencarian di table Sekolah
    private void hasilCariTableSekolahShow(ResultSet rs) throws SQLException {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID SEKOLAH");
        model.addColumn("NAMA SEKOLAH");
        model.addColumn("JENIS SEKOLAH");
        model.addColumn("ALAMAT SEKOLAH");            
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model.setRowCount(0);

        while(rs.next()) {
            Object [] data = {
                rs.getString("id_sekolah"),
                rs.getString("nama_sekolah"),
                rs.getString("jenis_sekolah"),
                rs.getString("alamat_sekolah")
            }; 
            model.addRow(data);
            tableSekolah.setModel(model);
        }
    }
    
    
    

