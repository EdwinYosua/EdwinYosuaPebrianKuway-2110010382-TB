    private void edtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCariKeyPressed
        
        try {
            String sql_comm;
            if(cbxCari.getSelectedItem().toString().equalsIgnoreCase("nisn_siswa") ||
               cbxCari.getSelectedItem().toString().equalsIgnoreCase("nama_siswa")) {
                
                sql_comm = "SELECT * FROM tb_siswa WHERE " 
                + cbxCari.getSelectedItem().toString() +
                " LIKE '%" + edtCari.getText() + "%'";
                
                hasilCariTableSiswaShow(cariData(sql_comm));
                
            }else if(cbxCari.getSelectedItem().toString().equalsIgnoreCase("id_sekolah") ||
                     cbxCari.getSelectedItem().toString().equalsIgnoreCase("nama_sekolah")) {
                
                sql_comm = "SELECT * FROM tb_sekolah WHERE " 
                + cbxCari.getSelectedItem().toString() +
                " LIKE '%" + edtCari.getText() + "%'";
                
                hasilCariTableSekolahShow(cariData(sql_comm));
            } 
            
        } catch (SQLException ex) {
            Logger.getLogger(MenuPendaftaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_edtCariKeyPressed

